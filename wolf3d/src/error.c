#include "wolf3d.h"

int		bounds_check(t_w3d *w)
{
	int i;

	i = 0;
	while (i < w->b_size.rows)
	{
		if (w->map[w->b_size.cols - 1][i] == '.' || (w->map[0][i] == '.'))
			return (0);
		i++;
	}
	i = 0;
	while (i < w->b_size.cols)
	{
		if (w->map[i][w->b_size.rows - 1] == '.' || (w->map[i][0] == '.'))
			return (0);
		i++;
	}
	return (1);
}
